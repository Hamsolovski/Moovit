import PropTypes from 'prop-types'
import "./trainingForm.css";
import { useState } from "react";

function TrainingForm({getEditForm, id, training}) {
  const api = import.meta.env.VITE_API_URL;

  const [title, setTitle] = useState(training.title);
  const [date, setDate] = useState(training.date);
  const [timeOfDay, setTimeOfDay] = useState(training.timeOfDay);
  const [duration, setDuration] = useState(training.duration);
  const [details, setDetails] = useState(training.details);
  const [sport, setSport] = useState(training.sport)

  // Fonction qui g√®re l'affichage du formulaire selon que l'utilisateur cr√©e ou √©dite son activit√©.

  const handleSubmit = (e) => {
    e.preventDefault();
    if (!getEditForm) { 
    fetch(`${api}/api/trainings`, {
      method: "POST",
      headers: {'Content-Type': 'application/json'},
      body: JSON.stringify({title, date, time_of_day: timeOfDay, duration, details, user_id: 3, sport_id: sport})
    })
  } else if (getEditForm) {
    fetch(`${api}/api/trainings/${id}`, {
      method: "PUT",
      headers: {'Content-Type': 'application/json'},
      body: JSON.stringify({title, date, time_of_day: timeOfDay, duration, details, user_id: 3, sport_id: sport})
    })
  }
  };

  const handleTitle = (e) => {
    e.preventDefault();
    setTitle(e.target.value);
  };

  const handleDate = (e) => {
    e.preventDefault();
    setDate(e.target.value);
  };

  const handleSport = (e) => {
    e.preventDefault();
    setSport(parseInt(e.target.value, 10))
  }

  const handleTime = (e) => {
    e.preventDefault();
    setTimeOfDay(e.target.value);
  };
  const handleDuration = (e) => {
    e.preventDefault();
    setDuration(e.target.value);
  };
  const handleDetails = (e) => {
    e.preventDefault();
    setDetails(e.target.value);
  };

  return (
    <form className="trainingForm" onSubmit={handleSubmit}>
      <h1>Cr√©er une nouvelle activit√©</h1>

      <input
        type="text"
        id="title"
        name="title"
        placeholder="Titre de ton entra√Ænement"
        value={title}
        onChange={handleTitle}
      />
      <input
        type="date"
        id="date"
        name="date"
        placeholder="Quel jour ?"
        value={date}
        onChange={handleDate}
      />
      <select
        type=""
        id="time-of-day-select"
        name="time-of-day"
        value={timeOfDay}
        onChange={handleTime}
      >
        <option>Matin, Apr√®s-midi ou Soir ? üòâ</option>
        <option>Matin</option>
        <option>Apr√®s-midi</option>
        <option>Soir</option>
      </select>
      <select id="sport-select" name="type" value={sport} onChange={handleSport}>
        <option>Quel sport ? ‚õπÔ∏è</option>
        <option value="4">Fitness</option>
        <option value="5">Running</option>
        <option value="6">Poney</option>
      </select>
      <input
        type="text"
        id="duration"
        name="duration"
        value={duration}
        placeholder="Combien de temps ?"
        onChange={handleDuration}
      />
      <textarea
        type="text"
        id="details"
        name="details"
        value={details}
        placeholder="Enregistre les d√©tails de ton activit√© ici üëå"
        onChange={handleDetails}
      />

      <button type="submit" className="primary-button">
        Enregistrer
      </button>
    </form>
  );
}

export default TrainingForm;

TrainingForm.propTypes = {
  getEditForm: PropTypes.bool.isRequired, // Indique si le formulaire est utilis√© pour √©diter une activit√© existante
  id: PropTypes.number.isRequired, // ID de l'activit√© en cours d'√©dition
  training: PropTypes.shape({
    title: PropTypes.string.isRequired, // Titre de l'activit√©
    date: PropTypes.string.isRequired, // Date de l'activit√©
    timeOfDay: PropTypes.string.isRequired, // Moment de la journ√©e de l'activit√©
    duration: PropTypes.string.isRequired, // Dur√©e de l'activit√©
    details: PropTypes.string.isRequired, // D√©tails de l'activit√©
    sport: PropTypes.number.isRequired // ID du sport associ√© √† l'activit√©
  }).isRequired
};